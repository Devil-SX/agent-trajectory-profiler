#!/bin/bash
# Agent Trajectory Profiler - Installation Script
# Creates wrapper scripts in ~/.local/bin so `agent-vis` can be called globally

set -e

# Configuration
INSTALL_DIR="$HOME/.local/bin"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$SCRIPT_DIR"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log() {
    local level=$1
    local message=$2
    local color=""

    case $level in
        "INFO")  color=$BLUE ;;
        "WARN")  color=$YELLOW ;;
        "ERROR") color=$RED ;;
        "SUCCESS") color=$GREEN ;;
    esac

    echo -e "${color}[$(date '+%H:%M:%S')] [$level] $message${NC}"
}

# Check dependencies
check_dependencies() {
    log "INFO" "Checking dependencies..."

    # Check uv
    if ! command -v uv &> /dev/null; then
        log "ERROR" "uv not found. Install with: curl -LsSf https://astral.sh/uv/install.sh | sh"
        exit 1
    fi
    log "INFO" "Found uv: $(uv --version)"

    # Sync project dependencies
    if [[ ! -d "$PROJECT_DIR/.venv" ]]; then
        log "INFO" "Initializing uv environment..."
        cd "$PROJECT_DIR"
        uv sync
    fi

    local python_version
    python_version=$(cd "$PROJECT_DIR" && uv run python3 -c 'import sys; print(f"{sys.version_info.major}.{sys.version_info.minor}")')
    log "INFO" "Found Python $python_version (via uv)"

    log "SUCCESS" "Dependencies check passed"
}

# Verify project structure
verify_project() {
    log "INFO" "Verifying project structure..."

    if [[ ! -f "$PROJECT_DIR/pyproject.toml" ]]; then
        log "ERROR" "pyproject.toml not found at $PROJECT_DIR"
        exit 1
    fi

    if [[ ! -d "$PROJECT_DIR/agent_vis" ]]; then
        log "ERROR" "agent_vis package not found at $PROJECT_DIR"
        exit 1
    fi

    if [[ ! -f "$PROJECT_DIR/agent_vis/cli/main.py" ]]; then
        log "ERROR" "CLI entry point not found at $PROJECT_DIR/agent_vis/cli/main.py"
        exit 1
    fi

    log "SUCCESS" "Project structure verified"
}

# Create installation directory
create_install_dir() {
    log "INFO" "Creating installation directory..."
    mkdir -p "$INSTALL_DIR"
    log "SUCCESS" "Directory ready: $INSTALL_DIR"
}

# Create command wrapper script
create_command() {
    log "INFO" "Creating command script..."

    cat > "$INSTALL_DIR/agent-vis" << EOF2
#!/bin/bash
# agent-vis: Agent Trajectory Profiler CLI
# Auto-generated by install.sh - DO NOT EDIT
# Project: $PROJECT_DIR

cd "$PROJECT_DIR"
exec uv run agent-vis "\$@"
EOF2

    chmod +x "$INSTALL_DIR/agent-vis"
    log "SUCCESS" "Command created: $INSTALL_DIR/agent-vis"
}

# Remove legacy command if present
cleanup_legacy_alias() {
    if [[ -f "$INSTALL_DIR/claude-vis" ]]; then
        rm -f "$INSTALL_DIR/claude-vis"
        log "INFO" "Removed legacy command: $INSTALL_DIR/claude-vis"
    fi
}

# Check PATH
check_path() {
    log "INFO" "Checking PATH configuration..."

    if [[ ":$PATH:" != *":$INSTALL_DIR:"* ]]; then
        log "WARN" "$INSTALL_DIR is not in your PATH"
        echo ""
        echo "Add this to your ~/.bashrc, ~/.zshrc, or ~/.profile:"
        echo "  export PATH=\"\$HOME/.local/bin:\$PATH\""
        echo ""
        echo "Then run: source ~/.bashrc (or restart your terminal)"
        echo ""
    else
        log "SUCCESS" "$INSTALL_DIR is already in PATH"
    fi
}

# Show help
show_help() {
    cat << HELP_EOF
Agent Trajectory Profiler - Installation Script

Usage: $0 [COMMAND]

Commands:
    install     Install agent-vis globally (default)
    --help      Show this help message

This script creates a wrapper script in ~/.local/bin that calls
the CLI using the project's uv environment at:
    $PROJECT_DIR

After installation, the following commands are available globally:

    agent-vis serve           Start web server with visualization dashboard
    agent-vis parse           Parse sessions and output JSON (headless CLI)
    agent-vis --help          Show all available options

To uninstall, run:
    $PROJECT_DIR/uninstall.sh

HELP_EOF
}

# Main installation
main() {
    echo ""
    log "INFO" "Installing Agent Trajectory Profiler..."
    log "INFO" "Project directory: $PROJECT_DIR"
    echo ""

    check_dependencies
    verify_project
    create_install_dir
    create_command
    cleanup_legacy_alias
    check_path

    echo ""
    log "SUCCESS" "Agent Trajectory Profiler installed successfully!"
    echo ""
    echo "Usage:"
    echo "  agent-vis serve                  Start web dashboard server"
    echo "  agent-vis parse                  Parse sessions to JSON (headless)"
    echo "  agent-vis parse --pretty -o out.json"
    echo "  agent-vis --help                 Show all options"
    echo ""
    echo "To uninstall:"
    echo "  $PROJECT_DIR/uninstall.sh"
    echo ""

    if [[ ":$PATH:" != *":$INSTALL_DIR:"* ]]; then
        log "WARN" "Don't forget to add $INSTALL_DIR to your PATH"
    fi
}

# Handle command line arguments
case "${1:-install}" in
    install)
        main
        ;;
    --help|-h|help)
        show_help
        ;;
    *)
        echo "Unknown command: $1"
        echo "Use --help for usage information"
        exit 1
        ;;
esac
